package finalProject2;

public class Effect {
    private Hide hide;
    private Show show;
    private Blink blink;
    private ChangeColor changeColor;
    private Jump jump;

    public Hide getHide() {
        return hide;
    }

    public void setHide(Hide hide) {
        this.hide = hide;
    }

    public Show getShow() {
        return show;
    }

    public void setShow(Show show) {
        this.show = show;
    }

    public Blink getBlink() {
        return blink;
    }

    public void setBlink(Blink blink) {
        this.blink = blink;
    }

    public ChangeColor getChangeColor() {
        return changeColor;
    }

    public void setChangeColor(ChangeColor changeColor) {
        this.changeColor = changeColor;
    }

    public Jump getJump() {
        return jump;
    }

    public void setJump(Jump jump) {
        this.jump = jump;
    }
    
}
